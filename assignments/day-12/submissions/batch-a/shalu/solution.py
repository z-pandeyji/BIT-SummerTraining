# Question 1: Main Types of Machine Learning
print("Supervised learning: learns from labeled data to make predictions.")
print("Unsupervised learning: finds patterns in data without labels.")
print("Reinforcement learning: learns by receiving rewards and penalties from actions.")

# Question 2: Supervised or Unsupervised?
print("Predicting a new house's price from previously sold houses: Supervised")
print("Grouping customers by shopping behaviour without predefined labels: Unsupervised")
print("Detecting spam using emails already labelled spam or not spam: Supervised")
print("Grouping news articles by topic without topic labels: Unsupervised")

# Question 3: Regression or Classification?
print("Predicting tomorrow's temperature: Regression")
print("Predicting whether a student will pass or fail: Classification")
print("Predicting the price of a second-hand bike: Regression")
print("Identifying whether an image contains a cat or a dog: Classification")

# Question 4: Loan Application
print("Loan application: Supervised")
print("Loan application: Classification")
print("This is supervised classification because the model learns from past labeled loan outcomes to decide whether a new application should be approved or rejected.")

# Question 5: Customer Segmentation
print("Customer segmentation: Unsupervised")
